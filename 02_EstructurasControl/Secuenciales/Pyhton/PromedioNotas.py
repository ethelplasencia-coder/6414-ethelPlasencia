print("CALCULAR PROMEDIO DE NOTAS")
print("==========================")

# Pedir las 3 notas
nota1 = float(input("Nota 1: "))
nota2 = float(input("Nota 2: "))
nota3 = float(input("Nota 3: "))

# Calcular el promedio
promedio = (nota1 + nota2 + nota3) / 3

# Mostrar resultado
print("==========================")
print("PROMEDIO:", format(promedio, ".2f"))


